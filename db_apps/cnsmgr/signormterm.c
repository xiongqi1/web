#include "signormterm.h"

/////////////////////////////////////////////////////////////////////////////////////////
static BOOL _fSignaled = FALSE;

/////////////////////////////////////////////////////////////////////////////////////////
BOOL signormterm_getTermSig(void)
{
	return _fSignaled;
}
/////////////////////////////////////////////////////////////////////////////////////////
void signormterm_sigHandler(int iSig)
{
	syslog(LOG_INFO, "signal detected - %d", iSig);

	_fSignaled = TRUE;
}
void signormterm_sigDummyHandler(int iSig)
{
	syslog(LOG_INFO, "dummy signal detected - %d", iSig);
}
/////////////////////////////////////////////////////////////////////////////////////////
void signormterm_resetTermSig(void)
{
	_fSignaled = FALSE;
}
/////////////////////////////////////////////////////////////////////////////////////////
void signormterm_ignoreSig(int iSig)
{
	signal(iSig, signormterm_sigDummyHandler);
}
/////////////////////////////////////////////////////////////////////////////////////////
void signormterm_regTermSig(int iSig)
{
	signal(iSig, signormterm_sigHandler);
}

/////////////////////////////////////////////////////////////////////////////////////////
